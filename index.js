// تولید متن
function generateText() {
    const textType = document.getElementById('text-type').value;
    const paragraphsCount = parseInt(document.getElementById('paragraphs').value);
    const selectedText = textTypes[textType];

    document.getElementById('selected-title').textContent = selectedText.title;

    let generatedHTML = '';
    for (let i = 0; i < paragraphsCount; i++) {
        const paragraphIndex = i % selectedText.paragraphs.length;
        generatedHTML += `<p>${selectedText.paragraphs[paragraphIndex]}</p>`;
    }

    document.getElementById('generated-text').innerHTML = generatedHTML;
}

// کپی متن
function copyText() {
    const textToCopy = document.getElementById('generated-text').innerText;
    navigator.clipboard.writeText(textToCopy).then(() => {
        const notification = document.getElementById('notification');
        notification.classList.add('show');
        setTimeout(() => { notification.classList.remove('show'); }, 3000);
    });
}

document.addEventListener('DOMContentLoaded', generateText);